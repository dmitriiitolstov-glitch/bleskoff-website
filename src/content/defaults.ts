export function mergeServices(cmsServices?: Service[]): Service[] {
	if (!cmsServices?.length) return defaultServices;

	const defaultsBySlug = new Map(defaultServices.map((service) => [service.slug, service]));
	const defaultsByTitle = new Map(defaultServices.map((service) => [service.title, service]));

	return cmsServices.map((service) => {
		const slug = service.slug || service.title.toLocaleLowerCase("ru-RU").replace(/[^a-z0-9а-яё]+/gi, "-").replace(/^-|-$/g, "");
		const fallback = defaultsBySlug.get(slug) ?? defaultsByTitle.get(service.title) ?? defaultServices[0];
		return {
			...fallback,
			...service,
			slug,
			image: service.image || fallback.image,
			outcomes: service.outcomes?.length ? service.outcomes : fallback.outcomes,
			included: service.included?.length ? service.included : fallback.included,
			priceOptions: service.priceOptions?.length ? service.priceOptions : fallback.priceOptions,
			process: service.process?.length ? service.process : fallback.process,
			faqs: service.faqs?.length ? service.faqs : fallback.faqs,
		};
	});
}

